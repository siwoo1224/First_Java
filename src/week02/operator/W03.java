package week02.operator;

public class W03 {
    // 비교연산자 예제
    public static void main(String[] args) {
        System.out.println(10 > 9); // 10이 9보다 크니? : 응! (true)
        System.out.println(10 >= 9); // 10이 9보다 크거나 같니? : 응! (true)
        System.out.println(10 < 9); // 10이 9보다 작니? : 아니! (false)
        System.out.println(10 <= 9); // 10이 9보다 작거나 같니?? : 아니! (false)
        System.out.println(10 == 10); // 10이 10이랑 같니? : 응! (true)
        System.out.println(10 == 9); // 10이 9랑 같니? : 아니! (false)
        System.out.println(10 != 9); // 10은 10이 아니니? : 아니! (fasle)
        System.out.println(10 != 9); // 10은 9가 아니니? : 응! (true)
        System.out.println("-----------------------------------------");
    }
}
